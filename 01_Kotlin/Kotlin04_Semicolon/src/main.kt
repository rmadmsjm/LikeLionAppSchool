// 세미콜론 : 프로그래밍 언어 중 문장의 마지막을 표시하기 위해 세미콜론(;)을 찍어야 하는 것이 있음
// 대다수의 개발자는 세미콜론을 통해 명령문을 구분하여 한 줄에 여러 개의 명령문을 작성하지 않고 한 줄에 하나의 명령문을 작성하는 것이 일반적
// 이에, 일부 프로그래밍 언어들은 세미콜론을 선택 사항으로 제공함

fun main() {
    println("세미콜론 없음")
    println("세미콜론 있음");

    // 만약 한 줄에 여러 명령문을 작성한다면 각각을 세미콜론으로 구분해야 함
    println("명령문1"); println("명령문2"); println("명령문3")
}